# Reverse the string "Hello World!" and print it
print("Hello World!"[::-1])  
# Output: !dlroW olleH

# Join and reverse the string
joined_string = ', '.join(['apple', 'banana', 'cherry'])
print(joined_string)  # Output: apple, banana, cherry

# Reverse the joined string
reversed_joined_string = joined_string[::-1]
print(reversed_joined_string)  # Output: yrrehc ,ananab ,elppa
