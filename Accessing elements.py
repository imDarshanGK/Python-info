# Initial tuple
tuple = (10, 20, 30, 40)

# Print first and second elements
print("First element:", tuple[0])
print("Second element:", tuple[1])

# New feature: printing the third and fourth elements
print("Third element:", tuple[2])
print("Fourth element:", tuple[3])

# Another new feature: displaying the length of the tuple
print("Length of the tuple:", len(tuple))
